public class TipoGradeBeans {

    private int idTipoGrade;
    private String dataAlt, descricaoTipoGrade;

    public int getIdTipoGrade() {
        return idTipoGrade;
    }

    public void setIdTipoGrade(int idTipoGrade) {
        this.idTipoGrade = idTipoGrade;
    }

    public String getDataAlt() {
        return dataAlt;
    }

    public void setDataAlt(String dataAlt) {
        this.dataAlt = dataAlt;
    }

    public String getDescricaoTipoGrade() {
        return descricaoTipoGrade;
    }

    public void setDescricaoTipoGrade(String descricaoTipoGrade) {
        this.descricaoTipoGrade = descricaoTipoGrade;
    }
}
